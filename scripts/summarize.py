def get_pt_comment(category: str) -> str:
    comments = PT_COMMENTS.get(category, PT_COMMENTS["自由テーマ"])
    return random.choice(comments)


def get_hashtags(category: str) -> list[str]:
    return HASHTAGS.get(category, HASHTAGS["自由テーマ"])


def format_category(category: str) -> str:
    emoji = CATEGORY_EMOJI.get(category, "📌")
    return f"{emoji} {category}"


def build_post_text(title: str, core: str, category: str) -> str:
    """リベシティ投稿用テキストを生成する"""
    tags = " ".join(get_hashtags(category))
    text = f"【今日の健康Tips】\n{title}\n\n{core}\n\n詳しくはプロフのリンクから✨\n\n{tags}"
    # 140〜200字に収める（超えた場合はcoreを短縮）
    if len(text) > 200:
        max_core = 200 - len(f"【今日の健康Tips】\n{title}\n\n\n\n詳しくはプロフのリンクから✨\n\n{tags}") - 3
        text = f"【今日の健康Tips】\n{title}\n\n{core[:max_core]}...\n\n詳しくはプロフのリンクから✨\n\n{tags}"
    return text


def pick_template(category: str) -> dict:
    """カテゴリに対応するTipsテンプレートをランダムに選ぶ"""
    templates = TIPS_TEMPLATES.get(category)
    if not templates:
        # 親カテゴリにフォールバック
        for key in TIPS_TEMPLATES:
            if key in category or category in key:
                templates = TIPS_TEMPLATES[key]
                break
    if not templates:
        templates = TIPS_TEMPLATES["自由テーマ"]
    return random.choice(templates)


def format_tip(article: dict, category: str) -> dict:
    """1記事をTips JSONフォーマットに整形する
    - Tips本文はカテゴリテンプレートから生成
    - PubMedの論文はエビデンスの引用元として表示
    """
    tmpl = pick_template(category)
    title = tmpl["title"]
    core = tmpl["core"]
    detail = tmpl["detail"]

    reliability = article.get("reliability", "★★★")
    pt_comment = get_pt_comment(category)
    post_text = build_post_text(title, core, category)
    tags = get_hashtags(category)

    # PubMedソース情報を付加
    source = article.get("source", "")
    article_link = article.get("link", "")
    if article_link:
        source = f"{source}（{article_link}）"

    return {
        "category": format_category(category),
        "title": title,
        "core": core,
        "detail": detail,
        "pt": pt_comment,
        "post": post_text,
        "source": source,
        "stars": reliability,
        "reliability": RELIABILITY_LABEL.get(reliability, "参考程度"),
        "tags": tags,
    }


def select_candidates(raw_data: dict) -> list[tuple[dict, str]]:
    """カテゴリごとに1件ずつ選ぶ（最大3カテゴリ）"""
    categories = raw_data["categories"]
    candidates = raw_data["candidates"]
    selected = []

    for category in categories[:3]:
        matched = [c for c in candidates if c.get("target_category") == category]
        if not matched:
            matched = [c for c in candidates if c.get("reliability") == "★★★"]
        if not matched:
            matched = candidates

        if matched:
            selected.append((matched[0], category))

    return selected


def process_date(date: datetime.date) -> Path | None:
    raw_path = OUTPUT_DIR / f"{date.isoformat()}_raw.json"
    if not raw_path.exists():
        print(f"RAWデータが見つかりません: {raw_path}")
        print("先に collect.py を実行してください")
        return None

    with open(raw_path, "r", encoding="utf-8") as f:
        raw_data = json.load(f)

    candidates = select_candidates(raw_data)
    tips = []
    for article, category in candidates:
        print(f"整形中: {category} / {article.get('title', '')[:30]}...")
        tip = format_tip(article, category)
        tips.append(tip)

    output = {
        "date": date.isoformat(),
        "tips": tips,
    }

    out_path = OUTPUT_DIR / f"{date.isoformat()}.json"
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    print(f"保存完了: {out_path}（{len(tips)} 本）")
    return out_path


def main():
    today = datetime.date.today()
    print(f"整形開始: {today}")
    process_date(today)


if __name__ == "__main__":
    main()
